package Capa_De_Negocios;

public class ObjetoClase {
	
	public String code;
	public String name;
	public boolean alter;
	public String cod_nivel;
	public String ruta;
	
	public ObjetoClase(String code, String name, boolean alter, String cod_nivel, String ruta){
		this.code = code;
		this.name = name;
		this.alter = alter;
		this.cod_nivel = cod_nivel;
		this.ruta = ruta;
	}

}
